package StringClass;

public class StringMethods {
    public static void main(String [] args){
        String s = "Mohammed Adnan";
        System.out.println(s.startsWith("Moh"));
        System.out.println(s.endsWith("nan"));
        System.out.println(s.contains("mmed"));
        System.out.println(s.concat(" hi"));
    }
}
